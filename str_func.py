def str_upper(text):
    """
    переводит текст в верхний регистр
    :param text:
    :return:
    """
    # egit bag
    return text.upper()


def str_title(text):
    """
    Каждое слово с большой буквы
    :param text: any text
    :return: text title
    """
    return text.title()


def str_lower(text):
    """
    Каждое слово с большой буквы
    :param text: any text
    :return: text title
    """
    return text.lower()
    


